// logical operators 

const statement1 = 5 > 3 // true
const statement2 = 40 < 3 // false

// OR statement (if there is true in statement it will choose true instead of false)
console.log(statement1 || statement2) // true

// AND statement (if both is not true becomes false)
console.log(statement1 && statement2) // false

// NOT statement
console.log(!statement1)
console.log(!statement2)

// numbers that are not 0 are true
// 0 false
console.log(!0)