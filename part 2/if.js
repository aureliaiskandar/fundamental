// "if - else if - else" accepts boolean

const name = 'Alven'

//basic IF statement
if (name === 'Alven'){
  console.log('hello Alven')
} else {
  console.log('hello stranger')
}

// if - else if statement
if (name === 'Alven') {
  console.log('hello Alven')
} else if (name === 'Andi'){
  console.log('hello Andi')
} else {
  console.log('hello stranger')
}